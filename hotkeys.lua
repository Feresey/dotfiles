local awful = require("awful")
local gears = require("gears")
local hotkeys_popup = require("awful.hotkeys_popup")
local menubar = require("menubar")

local function focus(dir)
    return function()
        awful.client.focus.bydirection(dir, client.focus, false)
    end
end

local function move_focused(dir)
    return function()
        awful.client.swap.bydirection(dir, client.focus, false)
    end
end

local keys = {}

keys.globalkeys = gears.table.join(
    awful.key({ modkey, }, "s", hotkeys_popup.show_help,
        { description = "show help", group = "awesome" }),
    awful.key({ modkey, }, "Left", awful.tag.viewprev,
        { description = "view previous", group = "tag" }),
    awful.key({ modkey, }, "Right", awful.tag.viewnext,
        { description = "view next", group = "tag" }),
    awful.key({ modkey, }, "Escape", awful.tag.history.restore,
        { description = "go back", group = "tag" }),

    -- move focus
    awful.key({ modkey, }, "j", focus("down"), { description = "focus down", group = "client" }),
    awful.key({ modkey, }, "k", focus("up"), { description = "focus up", group = "client" }),
    awful.key({ modkey, }, "h", focus("left"), { description = "focus left", group = "client" }),
    awful.key({ modkey, }, "l", focus("right"), { description = "focus right", group = "client" }),
    -- maybe useful
    awful.key({ modkey, }, "u", awful.client.urgent.jumpto, { description = "jump to urgent client", group = "client" }),
    awful.key({ modkey, }, "Tab",
        function()
            awful.client.focus.history.previous()
            if client.focus then
                client.focus:raise()
            end
        end,
        { description = "go back", group = "client" }),
